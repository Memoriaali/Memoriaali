/**
 * Check if a field name needs quotes in JavaScript object literal
 * @param {string} fieldName - The field name to check
 * @returns {boolean} - True if the field name needs quotes
 */
function needsQuotes(fieldName) {
  // Field names with dashes, spaces, or starting with numbers need quotes
  return /[^a-zA-Z_$]|^[0-9]/.test(fieldName);
}

