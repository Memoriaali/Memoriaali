  if (securityFields.neverExpose.includes(fieldName)) {
    return false;
  }
  
  // Admin-only fields are only safe for admin access
  if (securityFields.adminOnly.includes(fieldName) && accessLevel !== 'admin') {
    return false;
  }
  
  // Sensitive fields are only safe for owner and admin access
  if (securityFields.sensitive.includes(fieldName) && accessLevel === 'public') {
    return false;
  }
  
  return true;
