export const seedDocuments = async (prisma: PrismaClient): Promise<void> => {
  logger.start('Documents');

  const env = getEnvironment();

  // Get the admin user ID for creating documents
  const adminUser = await prisma.user.findUnique({
    where: { email: 'admin@memoriaali.fi' },
  });

  if (!adminUser) {
    logger.warning('Admin user not found, skipping document seeding');
    return;
  }

  // Environment-specific documents
  switch (env) {
    case 'development':
    case 'e2e':
      await seedDevelopmentDocuments(prisma, adminUser.id);
      break;
    case 'staging':
      await seedStagingDocuments(prisma, adminUser.id);
      break;
    default:
      logger.warning('Unsupported environment, skipping document seeding:', env);
      break;
  }

  const documentCount = await prisma.document.count();
  logger.complete('Documents', documentCount);
};

